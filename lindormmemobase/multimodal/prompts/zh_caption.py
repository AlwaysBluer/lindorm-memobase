"""图片描述生成提示词（中文版）。"""

DEFAULT_SYSTEM_PROMPT = """你是一位专业的图像分析师。
你的任务是生成准确、简洁的图片描述。"""


DEFAULT_CAPTION_PROMPT = """## 任务
用一段简洁的文字描述图片内容。

## 关注要点
1. **主要主体**：核心对象、人物或场景
2. **动作行为**：正在发生什么
3. **环境背景**：场景、环境、背景信息
4. **细节特征**：显著属性（颜色、衣着、表情、文字等）

## 描述规范
- 客观陈述事实
- 避免主观推测意图或情感
- 忽略无关紧要的细节
- 使用清晰简洁的语言

## 输出格式
一段简洁描述（2-4 句话）。

示例：一个人坐在木质书桌前，桌上有笔记本电脑和咖啡杯。书桌靠近窗户，自然光线充足。旁边散落着书籍和文件。这人看起来专注于自己的工作。"""


DETAILED_CAPTION_PROMPT = """## 任务
提供图片的全面详细描述。

## 关注要点
1. **人物**：人数、外貌、衣着、表情、动作
2. **物品**：出现的物品及其位置关系
3. **环境**：室内/室外、光线、场景、背景
4. **文字**：任何可见的文字或标识
5. **风格**：照片质量、角度、构图（如相关）

## 描述规范
- 全面但简洁
- 逻辑清晰（主体→动作→环境）
- 避免主观解读
- 标注图片中的任何文字

## 输出格式
一段详细描述（3-6 句话）。"""


def get_caption_prompt(detailed: bool = False) -> str:
    """获取图片描述提示词。

    Args:
        detailed: 是否返回详细版提示词。

    Returns:
        图片描述提示词。
    """
    return DETAILED_CAPTION_PROMPT if detailed else DEFAULT_CAPTION_PROMPT


def get_system_prompt() -> str:
    """获取系统提示词。"""
    return DEFAULT_SYSTEM_PROMPT
